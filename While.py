# ¿Què es la sentencia While?
'''Nos permite repetir una setencia un número infinito de veces. La sintaxis es:
while expresión
     sentencia'''
num = 10
while num > 0:
    num -= 1
    print (num)

# Claúsula else
num = 5
while num > 0:
    num -= 1
    print (num)
else:
    print ('Sentencia final del bucle')

# Bucles infinitos
num = 5
while num > 0:
    print ('Esto es infinito')


