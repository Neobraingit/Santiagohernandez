# ¿Qué es la Orientación a Objetos en Python?
'''Python es un lenguaje orientado a objetos y todas las estructuras que hemos visto son objetos'''

# ¿Qué son las clases?
'''Las clases son la estructura principal de la programación orientada a objetos y nos proporciona
un medio para agrupar datos y funcionalidades'''
class Coche:
    pass

# ¿Qué son los objetos?
'''Son instancias de nuestra clase'''
coche1 = Coche() # Creamos una instancia a partir de nuestra clase y se llama objeto
print (coche1)
coche2 = Coche()
print (coche2)
